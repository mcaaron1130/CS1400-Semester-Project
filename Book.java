public class Book {
    
    private String title;
    private String genre;

    public void setTitle(String inputTitle) {
       title = inputTitle;
    }

    public String getTitle() {
        return title;
    }

    public void setGenre(String inputGenre) {
       genre = inputGenre;
    }

    public String getGenre() {
        return genre;
    }
}
